package org.csu.metrics.domain;

public class OthersBean {
    String nom;
    String nopm;
    String nosm;
    String nof;
    String nopf;
    String nosf;
    String nosi;

    public OthersBean(String nom, String nopm, String nosm, String nof, String nopf, String nosf, String nosi) {
        this.nom = nom;
        this.nopm = nopm;
        this.nosm = nosm;
        this.nof = nof;
        this.nopf = nopf;
        this.nosf = nosf;
        this.nosi = nosi;
    }

    public String getNom() {
        return nom;
    }

    public void setNom(String nom) {
        this.nom = nom;
    }

    public String getNopm() {
        return nopm;
    }

    public void setNopm(String nopm) {
        this.nopm = nopm;
    }

    public String getNosm() {
        return nosm;
    }

    public void setNosm(String nosm) {
        this.nosm = nosm;
    }

    public String getNof() {
        return nof;
    }

    public void setNof(String nof) {
        this.nof = nof;
    }

    public String getNopf() {
        return nopf;
    }

    public void setNopf(String nopf) {
        this.nopf = nopf;
    }

    public String getNosf() {
        return nosf;
    }

    public void setNosf(String nosf) {
        this.nosf = nosf;
    }

    public String getNosi() {
        return nosi;
    }

    public void setNosi(String nosi) {
        this.nosi = nosi;
    }
}
